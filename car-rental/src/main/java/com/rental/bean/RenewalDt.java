package com.rental.bean;

public class RenewalDt {
	
	private int aid;
	private int rday;
	
	
	
	
	
	public RenewalDt() {
		super();
	}
	public RenewalDt(int aid, int rday) {
		super();
		this.aid = aid;
		this.rday = rday;
	}
	public int getAid() {
		return aid;
	}
	public void setAid(int aid) {
		this.aid = aid;
	}
	public int getRday() {
		return rday;
	}
	public void setRday(int rday) {
		this.rday = rday;
	}
	
	
	
	
}
